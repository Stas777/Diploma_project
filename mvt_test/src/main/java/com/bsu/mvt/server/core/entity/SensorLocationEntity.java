package com.bsu.mvt.server.core.entity;

public class SensorLocationEntity {
    private Long id;
    private String name;
    private Integer value;

    public SensorLocationEntity() {}

    public SensorLocationEntity(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getValue() {
        return value;
    }

    public void setValue(Integer value) {
        this.value = value;
    }
}
